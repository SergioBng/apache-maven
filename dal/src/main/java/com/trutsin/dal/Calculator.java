package com.trutsin.dal;

public class Calculator {
    public double add(final double firstValue, final double secondValue) {
        return firstValue + secondValue;
    }

    public double subtract(final double firstValue, final double secondValue) {
        return firstValue - secondValue;
    }

    public double multiply(final double firstValue, final double secondValue) {
        return firstValue * secondValue;
    }

    public double divide(final double firstValue, final double secondValue) {
        return firstValue / secondValue;
    }
}
